            <p class="blog-post-meta">November 21, 2016 by <a href="http://justincarver.com">Justin Carver</a></p>
			<p>Recently, I've been receiving a lot of e-mails regarding location settings and options.</p>
            <p>If your local weather or local settings on various websites are not "local" at all, then the location is likely coming
            from your ISP (Internet Service Provider) location. Unfortunately, this is common and impossible to fix.</p>
            <p>My best advice is to make sure you stay logged in on whatever site you are using, and that you set up any customization options pertaining to that site.<p>
            <p>For example, for local showtimes in Yahoo Movies, manually set up your favorite theatres, then stay logged in.</p>
