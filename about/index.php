            <h2>About BestHomepageEver</h2>
                <p>My name is Justin Carver, and this site began as a school project at the <a href="http://uoregon.edu">University of Oregon</a> (USA), in my very first computer class in 1999. The idea was to start a page that had links to all of the most commonly used sites for myself, friends, and family.</p>
                <p>As time progressed, I began seeing friends of friends using it at the University, so over time my goal was to continue to add sites that were relevant and useful for people to use in their daily lives.</p>            
                <p>Now, with a widespread national and new international userbase, I'm very proud and thankful to say that BHE is a useful tool for the world to enjoy.</p><p>Thanks for your support!</p>        
                <hr />
            <h2>Mission & Goals</h2>    
           	 <p>My goal has always been, and will continue to be, the following:</p>
                <ul>                    
                    <li>Usefulness</li>
                    <li>Simplicity</li>
                    <li>Speed</li>
                    <li>No Advertisements</li>
                </ul>
            <hr />
            <h3><img src="../pictures/about/check.png" /> Usefulness</h3><p>With each passing day, the Internet becomes a bigger part of our lives. Perhaps the most important goal of mine for <a href="../">BestHomepageEver</a> has been to create a page that gives you quick access to all the most important pages on the web.</p>
      		<h3><img src="../pictures/about/check.png" /> Simplicity</h3><p>As the Internet expands and gets more and more complex, <a href="../">BestHomepageEver</a> has and will adapt to maintain and achieve the simplicity that makes it more and more useful. Simplicity may not have all the bells and whistles of some sites, but it allows you to navigate the site, and the Internet, easier.</p>
            <h3><img src="../pictures/about/check.png" /> Speed</h3><p>The site is always improving its speed. I am constantly working to minimize load times, to help you (the user) be able to quickly go from your e-mail, to maps, to Google, to reading the news, all within a click each. Overall, my mission here is to allow you to bounce around the Internet through 1 portal, visiting all the sites you wish to as quick as possible.</p>
            <h3><img src="../pictures/about/check.png" /> No Advertising</h3><p>Last, but not least, one of the reasons why many of you use this site, is there are no pop-ups, no in-your-face advertisements, and no need to stare at products you won't be buying. I have never advertised with banners, and never will. This helps maintain a quicker and cleaner homepage for your enjoyment.</p>

          
          


