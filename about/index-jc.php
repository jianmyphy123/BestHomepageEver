                <p>My name is <a href="http://justincarver.com" target="new">Justin Carver</a>, and this site began as a school project at the <a href="http://uoregon.edu">University of Oregon</a> (USA), in my very first computer class in 1999. The homework assignment? Create a website.</p><p>Rather than creating something that would only be used for a week, I thought it might be fun to create a page page that had links to all of the most commonly used sites for myself, friends, and family.</p>
